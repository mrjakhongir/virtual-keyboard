function hideCaseDown() {
  caseDown.forEach((key) => {
    key.classList.add('hidden');
  });
}

function showCaseDown() {
  caseDown.forEach((key) => {
    key.classList.remove('hidden');
  });
}

function showCaseUp() {
  caseCaps.forEach((key) => {
    key.classList.remove('hidden');
  });
}
function hideCaseUp() {
  caseCaps.forEach((key) => {
    key.classList.add('hidden');
  });
}
// Set attributes on elements for selecting each key.
keys.forEach((key) => {
  let keycode;

  switch (key.innerText) {
    case 'Ctrl':
      keycode = 'Control';
      break;
    case 'Win':
      keycode = 'Meta';
      break;
    case 'Del':
      keycode = 'Delete';
      break;
    case '▲':
      keycode = 'ArrowUp';
      break;
    case '▼':
      keycode = 'ArrowDown';
      break;
    case '◄':
      keycode = 'ArrowLeft';
      break;
    case '►':
      keycode = 'ArrowRight';
      break;
    default:
      keycode = key.innerText;
  }

  key.setAttribute('keyname', keycode);
  key.setAttribute('upperCaseName', key.innerText.toUpperCase());
});

// Set active class to an element when key pressed.
window.addEventListener('keydown', (e) => {
  keys.forEach((key) => {
    if (
      e.key === key.getAttribute('keyname') ||
      e.key === key.getAttribute('upperCaseName')
    ) {
      key.classList.add('active');
    }
    if (e.code === 'Space') {
      spaceKey.classList.add('active');
    }
    if (e.code === 'ShiftLeft') {
      shiftRightKey.classList.remove('active');
    }
    if (e.code === 'ShiftRight') {
      shiftLeftKey.classList.remove('active');
    }
    if (e.code === 'AltLeft') {
      altRightKey.classList.remove('active');
    }
    if (e.code === 'AltRight') {
      altLeftKey.classList.remove('active');
    }
    if (e.code === 'ControlLeft') {
      controlRightKey.classList.remove('active');
    }
    if (e.code === 'ControlRight') {
      controlLeftKey.classList.remove('active');
    }
  });
});

window.addEventListener('keyup', (e) => {
  keys.forEach((key) => {
    if (
      e.key === key.getAttribute('keyname') ||
      e.key === key.getAttribute('upperCaseName')
    ) {
      key.classList.remove('active');
      key.classList.add('remove');
    }
    if (e.code === 'Space') {
      spaceKey.classList.remove('active');
      spaceKey.classList.add('remove');
    }
    if (e.code === 'ShiftLeft') {
      shiftLeftKey.classList.remove('active');
      shiftRightKey.classList.remove('remove');
    }
    if (e.code === 'ShiftRight') {
      shiftRightKey.classList.remove('active');
      shiftLeftKey.classList.remove('remove');
    }
    if (e.code === 'AltLeft') {
      altLeftKey.classList.remove('active');
      altRightKey.classList.remove('remove');
    }
    if (e.code === 'AltRight') {
      altRightKey.classList.remove('active');
      altLeftKey.classList.remove('remove');
    }
    if (e.code === 'ControlLeft') {
      controlLeftKey.classList.remove('active');
      controlRightKey.classList.remove('remove');
    }
    if (e.code === 'ControlRight') {
      controlRightKey.classList.remove('active');
      controlLeftKey.classList.remove('remove');
    }
    setTimeout(() => {
      key.classList.remove('remove');
    }, 200);
  });
});

